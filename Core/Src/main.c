/* USER CODE BEGIN Header_StartTemperatureReadTask */
/**
* @brief Function implementing the TemperatureRead thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTemperatureReadTask */
void StartTemperatureReadTask(void *argument)
{
  /* USER CODE BEGIN StartTemperatureReadTask */
  /* Infinite loop */
  for(;;)
  {
	  uint8_t address = 0x27 << 1;
	  uint8_t readCommand = 0x27 << 1 | 0x01;

	  HAL_I2C_Master_Transmit(&hi2c2, address, NULL, 0, HAL_MAX_DELAY);

	  HAL_I2C_Master_Transmit(&hi2c2, readCommand, NULL, 0, HAL_MAX_DELAY);
	  uint8_t data[4];
	  HAL_I2C_Master_Receive(&hi2c2, address, data, 4, HAL_MAX_DELAY);

	  uint16_t humidityRaw = (((uint16_t)(data[0] & 0x3F) << 8) | data[1]);
	  uint16_t temperatureRaw = (((uint16_t)data[2] << 6) | (data[3] >> 2));

	  const float humidityScaleFactor = 1.0 / ((1 << 14) - 2);
	  float humidity = humidityRaw * humidityScaleFactor * 100.0;

	  const float temperatureScaleFactor = 165.0 / ((1 << 14) - 2);
	  float temperature = temperatureRaw * temperatureScaleFactor - 40.0;

	  xQueueSend(tempMessage, &temperature, 0);
	  xQueueSend(humMessage, &humidity, 0);

	  osDelay(10000);
  }
  /* USER CODE END StartTemperatureReadTask */
}

/* USER CODE BEGIN Header_StartUVSensorTask */
/**
* @brief Function implementing the UVSensorTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartUVSensorTask */
void StartUVSensorTask(void *argument)
{
  /* USER CODE BEGIN StartUVSensorTask */
  /* Infinite loop */
  for(;;)
  {
	  HAL_ADC_Start(&hadc1);
	  HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);


	  uint16_t raw = HAL_ADC_GetValue(&hadc1);
	  float voltage = ( raw/65535.0 ) * 3.3;
	  float measurement; // In mW/cm2

	  if(voltage < 1.0)
	  {
		  measurement = 0;
	  }
	  else if(voltage > 2.8)
	  {
		  measurement = 15;
	  }
	  else
	  {
		  measurement = (voltage - 1.0) * (15 - 1.8);
	  }

	  xQueueSend(uvMessage, &measurement, 0);

	  char output[40];
	  sprintf(output, "UV level: %.2f mW/cm2\r\n", measurement);
	  HAL_UART_Transmit(&huart3, (uint8_t*)output, strlen(output), 100);


	  // Stop ADC conversion
	  HAL_ADC_Stop(&hadc1);
	  osDelay(5000);
  }
  /* USER CODE END StartUVSensorTask */
}

